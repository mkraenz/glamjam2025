<h2 class="h2-emphasis">How to make Bubble Tea</h2>

<p>
	To make Bubble Tea, the most important is doing things in <span class="inline-emphasis">
		the right order</span
	>.
</p>

<ol>
	<li>First, put the <span class="inline-emphasis">Cup</span> on the counter before you.</li>
	<li>
		Next, pour <span class="inline-emphasis">Strawberry Milk or Milk Tea</span> or add
		<span class="inline-emphasis">Tapioca Pearls</span>.<br /> The order doesn't matter in this step.
	</li>
	<li>
		Your customers expect good service so you add the <span class="inline-emphasis">Lid</span> next.
	</li>
	<li>
		Finally, no Bubble Tea is perfect without the <span class="inline-emphasis">Straw</span>.
	</li>
	<li>
		All that's left is to <span class="inline-emphasis">Serve</span> the Bubble Tea to your customer.
	</li>
</ol>
<p>
	Easy enough, right? The only problem are those tiny little chipmunks who <span
		class="inline-emphasis">change the placement</span
	> of the parts every time.
</p>
<p>
	Before I forget: You're customers will <span class="inline-emphasis">leave</span> if you make too many
	mistakes. I guess one or two mistakes are fine but no more than that.
</p>
